/** Given a URL pathname, return the equivalent path for the target language */
export function getAlternateUrl(pathname: string, targetLang: keyof typeof ui): string {
  // Strip trailing slash and language prefix to get the slug
  const clean = pathname.replace(/\/$/, '');
  const slug = clean.replace(/^\/(es|eu|en|fr)\//, '/').replace(/^\/(es|eu|en|fr)$/, '/').replace(/^\//, '');

  // Find which page key this slug belongs to
  for (const [, langSlugs] of Object.entries(routes)) {
    if (Object.values(langSlugs).includes(slug)) {
      const targetSlug = langSlugs[targetLang];
      if (targetLang === defaultLang) return `/${targetSlug}`;
      return `/${targetLang}/${targetSlug}`;
    }
  }

  // Fallback: just swap the prefix (for home or unknown pages)
  if (targetLang === defaultLang) return '/';
  return `/${targetLang}/`;
}
