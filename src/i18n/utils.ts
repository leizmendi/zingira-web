export function getLangFromUrl(url: URL) {
  const [, lang] = url.pathname.split('/');
  if (lang in ui) return lang as keyof typeof ui;
  return defaultLang;
}

export function useTranslations(lang: keyof typeof ui) {
  return function t(key: string): any {
    return (ui[lang] as any)[key] || (ui[defaultLang] as any)[key] || key;
  };
}

export function getLocalePath(lang: keyof typeof ui, path: string = '') {
  // Translate the slug if a route mapping exists (path is always the Spanish slug)
  let translatedPath = path;
  for (const langSlugs of Object.values(routes)) {
    if (langSlugs[defaultLang] === path) {
      translatedPath = langSlugs[lang];
      break;
    }
  }
  if (lang === defaultLang) return `/${translatedPath}`;
  return `/${lang}/${translatedPath}`;
}

